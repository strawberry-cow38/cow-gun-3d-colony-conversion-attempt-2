// Stem + suffix combos (strip a leading "-" if the stem already ends in a vowel).
for (const stem of stems) {
  for (const suf of suffixes) {
    const s = suf.startsWith('-') ? suf.slice(1) : suf;
    const joiner = suf.startsWith('-') ? '' : '';
    const name = (stem + joiner + s).trim();
    if (name.length >= 3 && name.length <= 24) pool.add(name);
  }
}

// A handful of honorifics / titles to round out variety.
for (const title of [
  'Sir',
  'Dame',
  'Lord',
  'Lady',
  'Admiral',
  'General',
  'Colonel',
  'Doctor',
  'Professor',
  'Reverend',
]) {
  for (const stem of stems.slice(0, 40)) pool.add(`${title} ${stem}`);
}

const sorted = Array.from(pool).sort((a, b) => a.localeCompare(b));
if (sorted.length < 1000) {
  throw new Error(`only generated ${sorted.length} names — add more seeds`);
}

// Shuffle deterministically for a fun distribution in the output file, then
// take the first 1000. Using a small linear-congruential so the output is
// reproducible run-to-run.
let seed = 0x5eedc0;
for (let i = sorted.length - 1; i > 0; i--) {
  seed = (seed * 1664525 + 1013904223) >>> 0;
  const j = seed % (i + 1);
  [sorted[i], sorted[j]] = [sorted[j], sorted[i]];
}
const names = sorted.slice(0, 1000);

writeFileSync(OUT, `${names.join('\n')}\n`, 'utf8');
console.log(`wrote ${names.length} names to ${OUT}`);
